func TestReconcileAllBalances(t *testing.T) {
	db, sMock, err := sqlmock.New()
	assert.NoError(t, err)
	defer db.Close()

	wRepo := &walletRepo.MockWalletRepository{}
	lRepo := &ledgerRepo.MockLedgerRepository{}
	s := NewScheduler(db, wRepo, lRepo)

	rows := sqlmock.NewRows([]string{"id", "user_id", "balance"}).
		AddRow("w1", "user1", "100.00").
		AddRow("w2", "user2", "200.00")
	sMock.ExpectQuery("SELECT id, user_id, balance FROM wallets").WillReturnRows(rows)

	lRepo.On("GetBalanceByWalletID", mock.Anything, "w1").Return(decimal.NewFromInt(100), nil)
	lRepo.On("GetBalanceByWalletID", mock.Anything, "w2").Return(decimal.NewFromInt(150), nil) // Mismatch!

	s.ReconcileAllBalances()

	lRepo.AssertExpectations(t)
}

