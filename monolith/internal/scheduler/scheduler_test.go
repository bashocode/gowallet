func TestSchedulerStartStop(t *testing.T) {
	db, _, err := sqlmock.New()
	assert.NoError(t, err)
	defer db.Close()

	wRepo := &walletRepo.MockWalletRepository{}
	lRepo := &ledgerRepo.MockLedgerRepository{}

	s := NewScheduler(db, wRepo, lRepo)
	assert.NotNil(t, s)

	s.Start()
	s.Stop()
}

