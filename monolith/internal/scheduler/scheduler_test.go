func TestExportDailyTransactions(t *testing.T) {
	db, sMock, err := sqlmock.New()
	assert.NoError(t, err)
	defer db.Close()

	wRepo := &walletRepo.MockWalletRepository{}
	lRepo := &ledgerRepo.MockLedgerRepository{}
	s := NewScheduler(db, wRepo, lRepo)

	now := time.Now().Format("2006-01-02 15:04:05")
	rows := sqlmock.NewRows([]string{"id", "sender_wallet_id", "receiver_wallet_id", "amount", "status", "created_at"}).
		AddRow("tx1", "w1", "w2", "50.00", "SUCCESS", now).
		AddRow("tx2", nil, "w2", "100.00", "SUCCESS", now)

	sMock.ExpectQuery("SELECT").WillReturnRows(rows)

	s.ExportDailyTransactions()

	// Clean up report files generated by the test
	filename := filepath.Join(
		"./reports",
		"transaction_report_"+time.Now().Format("20060102")+".csv",
	)
	_ = os.Remove(filename)
}
